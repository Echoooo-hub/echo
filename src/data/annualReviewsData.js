感谢这本书让我回顾了大学时代曾经认识的几位大家，让我了解当初看原著看得多么肤浅，让我从历史的角度了解思想的变迁，看到世界第一流的头脑如何反思和洞察这个时代，让我再次认识至今仍在发展的自由主义及其不可预知的未来，让我知道，思想的发展从未远离时代。现代思想并不是高高在上的理论，它可能是某个爱国主义宣传、某个人选择去海外工作生活、某对夫妻选择丁克、它可能是在俄乌的某个角落迫于生计辛勤耕作的农民，是美国重回制造业工厂的某个工人……

说实话，不知道看完书后我有没有达到刘擎老师的标准——做一个清醒的现代人（这本书我也看了，一般），但至少，当时看这本书的时候是很享受的。这就够了。

