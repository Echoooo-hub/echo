《翦商——殷周之变与华夏新生》

李硕



感谢丽阳大夫赠书。在让这本霸榜近些年书单的书在书架上好好躺了几个月后，我终于在去年工作最忙的时间段不慌不忙，慢条斯理看完了这场影响华夏文明的巨大变革。

周对中国古代历史的影响不言而喻，通常我们关注的是西周初创的制度及东周孕育出的春秋战国，但这本书将关注点前移。原来，周灭商以及人殉制度的终结，是真正影响文化走向的巨变。引用几句核心观点：“人祭宗教及角斗产业的消亡，都源于外来文化的干预。……周人并未开创一种新的宗教，而是采用世俗的人文主义立场，与极端的宗教行为保持距离，所以‘敬鬼神而远之。’这奠定了后世中国的文化基础。”“周文化与商文化很不同，族群性格也差别很大。商人直率冲动，思维灵活跳跃，有强者的自信与麻木；周人则隐忍含蓄，对外界更加关注和警觉，总担心尚未出现的危机和忧患。这是他们作为西陲小邦的生存之道。”“周人谨慎、谦恭，重集体，富于忧患意识，这些都成了新华夏族的样板品格。”“周公时代变革的最大结果，是神权退场，这让中国的文化过于‘早熟’；战国时代变革的最大结果，是贵族退场，这让中国的政治过于‘早熟’。”

在书中，作者多少带有些贬低商的意味，全书有一个基本出发点，即基于现代人的观念对人殉制度的恐惧与否定。这也是这本书有些争议的所在，毕竟那个时代，哪个文明能摆脱对神权的恐惧与敬畏？人祭制度的存在是非常合理的。甚至到了大航海时代，与世隔绝的阿兹特克依然保留了‘野蛮’的人祭制度。然而，看完本书，我基本是从无到有重塑了对商的认知。这是一个伟大的文明。

