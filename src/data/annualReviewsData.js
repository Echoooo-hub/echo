看这本书还有一个印象深刻的点，在于气候变化。对，就是我们现在“津津乐道”的气候变化。我惊讶地发现，仅仅过了三千多年，我们的气候发生了翻天覆地的变化。在商代，“上族人传统的家畜是水牛，偏爱豢养的动物是大象，且殷都的气候本来就很适合这两种动物”。这在我们看来简直无法想象，河南盛产大象？另外当时黄河流域，最重要的自然改造运动是改造湿地，开发平原，以生产粮食。如此来看，像地球变暖这样的气候变化就如科学家所论证的，到底是人为还是自然的结果呢？作为一种生物，顺应自然更关乎生存繁衍，只是，人又为精神所主宰，相信什么更重要，不是么？





